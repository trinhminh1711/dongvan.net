                    {{ isExpanded ?
                        'Thu gọn' : 'Xem thêm' }}</a>
                <div class="mt-5">
                    <div class="d-flex justify-content-between align-items-center mb-3">
                        <h3 class="fw-bold">Danh sách chương ( {{ fullStoryData.data.length }})</h3>
